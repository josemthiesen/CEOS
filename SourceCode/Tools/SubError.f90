subroutine Error(msg)

    !************************************************************************************
    ! DECLARATIONS OF VARIABLES
    !************************************************************************************
    ! Modules and implicit declarations
    ! -----------------------------------------------------------------------------------
    implicit none 
    
    ! Input variables
    ! -----------------------------------------------------------------------------------
    character(len=*) msg
    
    !************************************************************************************
    
    !************************************************************************************
    ! WRITING THE MESSAGE AND STOP THE PROGRAMM
    !************************************************************************************           
    
    write(*,*) trim(msg)
    stop
    !************************************************************************************           
    
end subroutine